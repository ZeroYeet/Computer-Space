-- =========================
-- 1. TẠO DATABASE
-- =========================
DROP DATABASE IF EXISTS ComputerSpace;
CREATE DATABASE ComputerSpace
CHARACTER SET utf8mb4
COLLATE utf8mb4_unicode_ci;

USE ComputerSpace;

SET FOREIGN_KEY_CHECKS = 0;

-- =========================
-- 2. BẢNG TÀI KHOẢN
-- =========================
CREATE TABLE TAI_KHOAN (
    MaTK INT AUTO_INCREMENT PRIMARY KEY,
    TenDangNhap VARCHAR(50) NOT NULL UNIQUE,
    MatKhau VARCHAR(255) NOT NULL,
    VaiTro ENUM('KHACH_HANG', 'QUAN_TRI_VIEN') NOT NULL,
    TrangThai TINYINT NOT NULL DEFAULT 1,
    NgayTao DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    NgayDangNhapCuoi DATETIME NULL
) ENGINE=InnoDB;

CREATE TABLE KHACH_HANG (
    MaKH INT AUTO_INCREMENT PRIMARY KEY,
    MaTK INT NOT NULL UNIQUE,
    HoTen VARCHAR(100) NOT NULL,
    SDT VARCHAR(15) UNIQUE,
    Email VARCHAR(100) UNIQUE,
    DiaChi VARCHAR(255),
    CONSTRAINT FK_KHACH_HANG_TAI_KHOAN
        FOREIGN KEY (MaTK) REFERENCES TAI_KHOAN(MaTK)
        ON DELETE CASCADE
        ON UPDATE CASCADE
) ENGINE=InnoDB;

CREATE TABLE QUAN_TRI_VIEN (
    MaNV INT AUTO_INCREMENT PRIMARY KEY,
    MaTK INT NOT NULL UNIQUE,
    HoTen VARCHAR(100) NOT NULL,
    SDT VARCHAR(15) UNIQUE,
    Email VARCHAR(100) UNIQUE,
    ChucVu VARCHAR(100),
    CONSTRAINT FK_QUAN_TRI_VIEN_TAI_KHOAN
        FOREIGN KEY (MaTK) REFERENCES TAI_KHOAN(MaTK)
        ON DELETE CASCADE
        ON UPDATE CASCADE
) ENGINE=InnoDB;

-- =========================
-- 3. BẢNG SẢN PHẨM
-- =========================
CREATE TABLE LOAI_SAN_PHAM (
    MaLoaiSP INT AUTO_INCREMENT PRIMARY KEY,
    TenLoaiSP VARCHAR(100) NOT NULL UNIQUE,
    MoTa TEXT
) ENGINE=InnoDB;

CREATE TABLE SAN_PHAM (
    MaSP INT AUTO_INCREMENT PRIMARY KEY,
    TenSP VARCHAR(255) NOT NULL,
    MaLoaiSP INT NOT NULL,
    ThuongHieu VARCHAR(100),
    GiaBan DECIMAL(15,2) NOT NULL DEFAULT 0,
    SoLuongTon INT NOT NULL DEFAULT 0,
    BaoHanhThang INT NOT NULL DEFAULT 0,
    MoTaNgan TEXT,
    MoTaChiTiet LONGTEXT,
    TrangThai TINYINT NOT NULL DEFAULT 1,
    NgayTao DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    NgayCapNhat DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    CONSTRAINT FK_SAN_PHAM_LOAI
        FOREIGN KEY (MaLoaiSP) REFERENCES LOAI_SAN_PHAM(MaLoaiSP)
        ON DELETE RESTRICT
        ON UPDATE CASCADE
) ENGINE=InnoDB;

CREATE INDEX IDX_SAN_PHAM_MaLoaiSP ON SAN_PHAM(MaLoaiSP);
CREATE FULLTEXT INDEX IDX_SAN_PHAM_TenSP ON SAN_PHAM(TenSP);
CREATE INDEX IDX_SAN_PHAM_ThuongHieu ON SAN_PHAM(ThuongHieu);

CREATE TABLE ANH_SAN_PHAM (
    MaAnh INT AUTO_INCREMENT PRIMARY KEY,
    MaSP INT NOT NULL,
    DuongDanAnh VARCHAR(255) NOT NULL,
    LaAnhDaiDien TINYINT NOT NULL DEFAULT 0,
    CONSTRAINT FK_ANH_SAN_PHAM_SAN_PHAM
        FOREIGN KEY (MaSP) REFERENCES SAN_PHAM(MaSP)
        ON DELETE CASCADE
        ON UPDATE CASCADE
) ENGINE=InnoDB;

CREATE INDEX IDX_ANH_SAN_PHAM_MaSP ON ANH_SAN_PHAM(MaSP);

