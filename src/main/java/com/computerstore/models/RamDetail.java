package com.computerstore.models;

public class RamDetail {
	private int maSP;
	private String loaiRam;
	private String dungLuong;
	private String busRam;
	private String dienAp;
	private int soThanh;

	public int getMaSP() {
		return maSP;
	}

	public void setMaSP(int maSP) {
		this.maSP = maSP;
	}

	public String getLoaiRam() {
		return loaiRam;
	}

	public void setLoaiRam(String loaiRam) {
		this.loaiRam = loaiRam;
	}

	public String getDungLuong() {
		return dungLuong;
	}

	public void setDungLuong(String dungLuong) {
		this.dungLuong = dungLuong;
	}

	public String getBusRam() {
		return busRam;
	}

	public void setBusRam(String busRam) {
		this.busRam = busRam;
	}

	public String getDienAp() {
		return dienAp;
	}

	public void setDienAp(String dienAp) {
		this.dienAp = dienAp;
	}

	public int getSoThanh() {
		return soThanh;
	}

	public void setSoThanh(int soThanh) {
		this.soThanh = soThanh;
	}
}
