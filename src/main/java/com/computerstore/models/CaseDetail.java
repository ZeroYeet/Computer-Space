package com.computerstore.models;

public class CaseDetail {
	private int maSP;
	private String hoTroMain;
	private String mauSac;
	private String chatLieu;
	private String kichThuoc;
	private String hoTroNguon;
	private int soLuongQuatHoTro;
	private boolean coKinhCuongLuc;
	private String congTruocCase;

	public int getMaSP() {
		return maSP;
	}

	public void setMaSP(int maSP) {
		this.maSP = maSP;
	}

	public String getHoTroMain() {
		return hoTroMain;
	}

	public void setHoTroMain(String hoTroMain) {
		this.hoTroMain = hoTroMain;
	}

	public String getMauSac() {
		return mauSac;
	}

	public void setMauSac(String mauSac) {
		this.mauSac = mauSac;
	}

	public String getChatLieu() {
		return chatLieu;
	}

	public void setChatLieu(String chatLieu) {
		this.chatLieu = chatLieu;
	}

	public String getKichThuoc() {
		return kichThuoc;
	}

	public void setKichThuoc(String kichThuoc) {
		this.kichThuoc = kichThuoc;
	}

	public String getHoTroNguon() {
		return hoTroNguon;
	}

	public void setHoTroNguon(String hoTroNguon) {
		this.hoTroNguon = hoTroNguon;
	}

	public int getSoLuongQuatHoTro() {
		return soLuongQuatHoTro;
	}

	public void setSoLuongQuatHoTro(int soLuongQuatHoTro) {
		this.soLuongQuatHoTro = soLuongQuatHoTro;
	}

	public boolean isCoKinhCuongLuc() {
		return coKinhCuongLuc;
	}

	public void setCoKinhCuongLuc(boolean coKinhCuongLuc) {
		this.coKinhCuongLuc = coKinhCuongLuc;
	}

	public String getCongTruocCase() {
		return congTruocCase;
	}

	public void setCongTruocCase(String congTruocCase) {
		this.congTruocCase = congTruocCase;
	}
}
