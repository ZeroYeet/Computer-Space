-- =========================
-- 5. GIỎ HÀNG
-- =========================
CREATE TABLE GIO_HANG (
    MaGioHang INT AUTO_INCREMENT PRIMARY KEY,
    MaKH INT NOT NULL UNIQUE,
    NgayCapNhat DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    CONSTRAINT FK_GIO_HANG_KHACH_HANG
        FOREIGN KEY (MaKH) REFERENCES KHACH_HANG(MaKH)
        ON DELETE CASCADE
        ON UPDATE CASCADE
) ENGINE=InnoDB;

CREATE TABLE CHI_TIET_GIO_HANG (
    MaGioHang INT NOT NULL,
    MaSP INT NOT NULL,
    SoLuong INT NOT NULL DEFAULT 1,
    DonGia DECIMAL(15,2) NOT NULL DEFAULT 0,
    PRIMARY KEY (MaGioHang, MaSP),
    CONSTRAINT FK_CTGH_GIO_HANG
        FOREIGN KEY (MaGioHang) REFERENCES GIO_HANG(MaGioHang)
        ON DELETE CASCADE
        ON UPDATE CASCADE,
    CONSTRAINT FK_CTGH_SAN_PHAM
        FOREIGN KEY (MaSP) REFERENCES SAN_PHAM(MaSP)
        ON DELETE CASCADE
        ON UPDATE CASCADE
) ENGINE=InnoDB;

CREATE INDEX IDX_CTGH_MaSP ON CHI_TIET_GIO_HANG(MaSP);

-- =========================
-- 6. ĐƠN HÀNG
-- =========================
CREATE TABLE DON_HANG (
    MaDonHang INT AUTO_INCREMENT PRIMARY KEY,
    MaKH INT NOT NULL,
    MaNV INT NULL,
    NgayDat DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    DiaChiNhan VARCHAR(255) NOT NULL,
    SDTNhan VARCHAR(15) NOT NULL,
    TongTien DECIMAL(15,2) NOT NULL DEFAULT 0,
    PhiVanChuyen DECIMAL(15,2) NOT NULL DEFAULT 0,
    TrangThaiDonHang ENUM('CHO_XAC_NHAN', 'DA_XAC_NHAN', 'DANG_GIAO', 'DA_GIAO', 'DA_HUY') NOT NULL DEFAULT 'CHO_XAC_NHAN',
    GhiChu TEXT,
    MaKM INT NULL,
    CONSTRAINT FK_DON_HANG_KHACH_HANG
        FOREIGN KEY (MaKH) REFERENCES KHACH_HANG(MaKH)
        ON DELETE RESTRICT
        ON UPDATE CASCADE,
    CONSTRAINT FK_DON_HANG_QUAN_TRI_VIEN
        FOREIGN KEY (MaNV) REFERENCES QUAN_TRI_VIEN(MaNV)
        ON DELETE SET NULL
        ON UPDATE CASCADE,
    CONSTRAINT FK_DON_HANG_KHUYEN_MAI
        FOREIGN KEY (MaKM) REFERENCES KHUYEN_MAI(MaKM)
        ON DELETE SET NULL
        ON UPDATE CASCADE
) ENGINE=InnoDB;

CREATE INDEX IDX_DON_HANG_MaKH ON DON_HANG(MaKH);
CREATE INDEX IDX_DON_HANG_MaNV ON DON_HANG(MaNV);

CREATE TABLE CHI_TIET_DON_HANG (
    MaDonHang INT NOT NULL,
    MaSP INT NOT NULL,
    SoLuong INT NOT NULL,
    DonGia DECIMAL(15,2) NOT NULL,
    ThanhTien DECIMAL(15,2) NOT NULL,
    PRIMARY KEY (MaDonHang, MaSP),
    CONSTRAINT FK_CTDH_DON_HANG
        FOREIGN KEY (MaDonHang) REFERENCES DON_HANG(MaDonHang)
        ON DELETE CASCADE
        ON UPDATE CASCADE,
    CONSTRAINT FK_CTDH_SAN_PHAM
        FOREIGN KEY (MaSP) REFERENCES SAN_PHAM(MaSP)
        ON DELETE RESTRICT
        ON UPDATE CASCADE
) ENGINE=InnoDB;

CREATE INDEX IDX_CTDH_MaSP ON CHI_TIET_DON_HANG(MaSP);

-- =========================
-- 7. THANH TOÁN
-- =========================
CREATE TABLE PHUONG_THUC_THANH_TOAN (
    MaPTTT INT AUTO_INCREMENT PRIMARY KEY,
    TenPTTT VARCHAR(100) NOT NULL UNIQUE,
    MoTa TEXT,
    TrangThai TINYINT NOT NULL DEFAULT 1
) ENGINE=InnoDB;

CREATE TABLE THANH_TOAN (
    MaThanhToan INT AUTO_INCREMENT PRIMARY KEY,
    MaDonHang INT NOT NULL UNIQUE,
    MaPTTT INT NOT NULL,
    SoTien DECIMAL(15,2) NOT NULL,
    ThoiGianThanhToan DATETIME DEFAULT CURRENT_TIMESTAMP,
    TrangThaiThanhToan ENUM('CHUA_THANH_TOAN', 'DA_THANH_TOAN', 'HOAN_TIEN') NOT NULL DEFAULT 'CHUA_THANH_TOAN',
    MaGiaoDich VARCHAR(100),
    TenNganHang VARCHAR(100),
    CONSTRAINT FK_THANH_TOAN_DON_HANG
        FOREIGN KEY (MaDonHang) REFERENCES DON_HANG(MaDonHang)
        ON DELETE CASCADE
        ON UPDATE CASCADE,
    CONSTRAINT FK_THANH_TOAN_PTTT
        FOREIGN KEY (MaPTTT) REFERENCES PHUONG_THUC_THANH_TOAN(MaPTTT)
        ON DELETE RESTRICT
        ON UPDATE CASCADE
) ENGINE=InnoDB;

CREATE INDEX IDX_THANH_TOAN_MaPTTT ON THANH_TOAN(MaPTTT);

-- =========================
-- 8. KHUYẾN MÃI
-- =========================
CREATE TABLE KHUYEN_MAI (
    MaKM INT AUTO_INCREMENT PRIMARY KEY,
    TenKM VARCHAR(150) NOT NULL,
    LoaiGiam ENUM('PHAN_TRAM', 'SO_TIEN') NOT NULL,
    GiaTriGiam DECIMAL(15,2) NOT NULL DEFAULT 0,
    NgayBatDau DATETIME NOT NULL,
    NgayKetThuc DATETIME NOT NULL,
    TrangThai TINYINT NOT NULL DEFAULT 1,
    MaNV INT NULL,
    CONSTRAINT FK_KHUYEN_MAI_QUAN_TRI_VIEN
        FOREIGN KEY (MaNV) REFERENCES QUAN_TRI_VIEN(MaNV)
        ON DELETE SET NULL
        ON UPDATE CASCADE
) ENGINE=InnoDB;

CREATE INDEX IDX_KHUYEN_MAI_MaNV ON KHUYEN_MAI(MaNV);

CREATE TABLE KHUYEN_MAI_SAN_PHAM (
    MaKM INT NOT NULL,
    MaSP INT NOT NULL,
    PRIMARY KEY (MaKM, MaSP),
    CONSTRAINT FK_KMSP_KHUYEN_MAI
        FOREIGN KEY (MaKM) REFERENCES KHUYEN_MAI(MaKM)
        ON DELETE CASCADE
        ON UPDATE CASCADE,
    CONSTRAINT FK_KMSP_SAN_PHAM
        FOREIGN KEY (MaSP) REFERENCES SAN_PHAM(MaSP)
        ON DELETE CASCADE
        ON UPDATE CASCADE
) ENGINE=InnoDB;

CREATE INDEX IDX_KMSP_MaSP ON KHUYEN_MAI_SAN_PHAM(MaSP);

-- =========================
-- 9. ĐÁNH GIÁ
-- =========================
CREATE TABLE DANH_GIA (
    MaDG INT AUTO_INCREMENT PRIMARY KEY,
    MaKH INT NOT NULL,
    MaSP INT NOT NULL,
    SoSao TINYINT NOT NULL,
    NoiDung TEXT,
    NgayDanhGia DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    TrangThaiDuyet TINYINT NOT NULL DEFAULT 0,
    CONSTRAINT FK_DANH_GIA_KHACH_HANG
        FOREIGN KEY (MaKH) REFERENCES KHACH_HANG(MaKH)
        ON DELETE CASCADE
        ON UPDATE CASCADE,
    CONSTRAINT FK_DANH_GIA_SAN_PHAM
        FOREIGN KEY (MaSP) REFERENCES SAN_PHAM(MaSP)
        ON DELETE CASCADE
        ON UPDATE CASCADE,
    CONSTRAINT UQ_DANH_GIA UNIQUE (MaKH, MaSP)
) ENGINE=InnoDB;

CREATE INDEX IDX_DANH_GIA_MaSP ON DANH_GIA(MaSP);

